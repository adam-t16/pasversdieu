function displaySurahs(surahs = quranData.surahs) {
    const start = (currentPage - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const paginatedSurahs = surahs.slice(start, end);
    
    const surahList = document.getElementById('surah-list');
    surahList.innerHTML = '';
    paginatedSurahs.forEach(surah => {
        surahList.appendChild(createSurahCard(surah));
    });
    
    updatePaginationControls(surahs.length);
}

function updatePaginationControls(totalItems) {
    const totalPages = Math.ceil(totalItems / itemsPerPage);
    document.getElementById('page-info').textContent = `${currentPage} / ${totalPages}`;
    
    document.getElementById('prev-page').disabled = currentPage === 1;
    document.getElementById('next-page').disabled = currentPage === totalPages;
}

// Adkar Display
function showAdkar(category) {
    const content = document.getElementById('adkar-content');
    content.innerHTML = quranData.adkar[category].map(dhikr => `
        <div class="dhikr-card">
            <div class="dhikr-text">${dhikr.text}</div>
            <div class="dhikr-translation">${dhikr.translation}</div>
            <div class="dhikr-repeat">التكرار: ${dhikr.repeat}</div>
        </div>
    `).join('');
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    displaySurahs();
    
    document.querySelectorAll('.adkar-category').forEach(category => {
        category.addEventListener('click', (e) => {
            document.querySelectorAll('.adkar-category').forEach(c => c.classList.remove('active'));
            e.target.classList.add('active');
            showAdkar(e.target.dataset.category);
        });
    });
    
    showAdkar('morning');
    
    // Modal close button
    document.querySelector('.close').addEventListener('click', () => {
        const modal = document.getElementById('modal');
        modal.style.display = 'none';
        if (currentAudio) {
            currentAudio.pause();
            isPlaying = false;
        }
    });
});

// Close modal when clicking outside
window.addEventListener('click', (e) => {
    const modal = document.getElementById('modal');
    if (e.target === modal) {
        modal.style.display = 'none';
        if (currentAudio) {
            currentAudio.pause();
            isPlaying = false;
        }
    }
});

// Pagination Controls
document.getElementById('prev-page').addEventListener('click', () => {
    if (currentPage > 1) {
        currentPage--;
        displaySurahs();
    }
});

document.getElementById('next-page').addEventListener('click', () => {
    const totalPages = Math.ceil(quranData.surahs.length / itemsPerPage);
    if (currentPage < totalPages) {
        currentPage++;
        displaySurahs();
    }
});
